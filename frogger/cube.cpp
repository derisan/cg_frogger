#include "cube.h"

#include <GL/glew.h>

#include "vertexarray.h"

Cube::Cube(Game* game)
	: Object{ game },
    mVertexArray{ nullptr }
{
    Load();
}

Cube::~Cube()
{
	delete mVertexArray;
}

void Cube::Update()
{
	if (GetState() == State::kActive)
        Object::Update();
}

void Cube::Draw(Shader* shader)
{
	Object::Draw(shader);

	mVertexArray->SetActive();
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

