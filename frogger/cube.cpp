#include "cube.h"

#include <GL/glew.h>

#include "vertexarray.h"

Cube::Cube(Game* game)
	: Actor{ game },
    mVertexArray{ nullptr }
{
    Load();
}

Cube::~Cube()
{
	delete mVertexArray;
}

void Cube::UpdateObject()
{
    Actor::UpdateActor();
}

void Cube::Draw(Shader* shader)
{
	Actor::Draw(shader);

	mVertexArray->SetActive();
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

